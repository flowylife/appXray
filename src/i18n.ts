export function loadLanguage(storage: Storage = window.localStorage): AppLanguage {
  const stored = storage.getItem(LANGUAGE_STORAGE_KEY);
  return stored === "en" || stored === "ko" ? stored : DEFAULT_LANGUAGE;
}

export function saveLanguage(language: AppLanguage, storage: Storage = window.localStorage): void {
  storage.setItem(LANGUAGE_STORAGE_KEY, language);
}

export function createTranslator(language: AppLanguage): Translator {
  return (key, values = {}) => {
    let result: string = MESSAGES[language][key] ?? MESSAGES[DEFAULT_LANGUAGE][key] ?? key;
    for (const [name, value] of Object.entries(values)) {
      result = result.replaceAll(`{${name}}`, String(value));
    }
    return result;
  };
}

