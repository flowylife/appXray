export function formatDateTimeForLanguage(language: AppLanguage, value: string): string {
  return new Intl.DateTimeFormat(language === "ko" ? "ko-KR" : "en-US", {
    dateStyle: "short",
    timeStyle: "short",
  }).format(new Date(value));
}

export function searchLocale(language: AppLanguage): string {
  return language === "ko" ? "ko-KR" : "en-US";
}
